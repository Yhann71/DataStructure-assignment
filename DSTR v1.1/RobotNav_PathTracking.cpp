//stack
stack::stack() {
    top = -1;
}

//Adds a new step movement on top of the stack
bool stack::push(stepMovement step) {
    if (isFull()) {
        cout << "Stack is full, cannot record more steps." << endl;
        return false;
    }
    steps[++top] = step;
    return true;
}

//Removes and returns the top step movement
stepMovement stack::pop() {
    if (isEmpty()) {
        cout << "Stack is empty. No steps to pop" << endl;
        stepMovement empty;
        empty.stepNumber = -1;
        empty.zone = "";
        empty.aisle = "";
        empty.shelf = "";
        return empty;
    }
    return steps[top--];
}

//Returns the top step movement without removing it
stepMovement stack::peek() {
    if (isEmpty()) {
        cout << "Stack is empty. No steps to peek" << endl;
        stepMovement empty;
        empty.stepNumber = -1;
        empty.zone = "";
        empty.aisle = "";
        empty.shelf = "";
        return empty;
    }
    return steps[top];
}

bool stack::isEmpty() {
    return top == -1;
}

bool stack::isFull() {
    return top == max_Steps -1;
}

int stack::getSize() {
    return top + 1;
}

//Prints all steps for the forward path
void stack::displayAll() {
    if (isEmpty()) {
        cout << "No steps recorded." << endl;
        return; 
    }

    cout << left
        << setw(8) << "Step"
        << setw(16) << "Zone"
        << setw(16) << "Aisle"
        << setw(16) << "Shelf" << endl;
    cout << "--------------------------------------------------------------------------------" << endl;

    for (int i = 0; i <= top; i++) {
        cout << left
            << setw(8) << steps[i].stepNumber
            << setw(16) << steps[i].zone
            << setw(16) << steps[i].aisle
            << setw(16) << (steps[i].shelf.empty() ? "-" : steps[i].shelf) << endl;
    }
}

