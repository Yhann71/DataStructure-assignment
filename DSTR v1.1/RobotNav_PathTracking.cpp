//A new navigation session is initialised using data from other tasks
//Takes the route array generated by Task 5 and pushes each step onto stack to record forward path
void NavigationSystem::startPath(string oID, string rID, string iZone, string iAisle,string iShelf,
                                string dest, stepMovement route[], int routeSize) {
    resetNavigation();
 
    orderID = oID;
    robotID = rID;
    itemZone = iZone;
    itemAisle = iAisle;
    itemShelf = iShelf;
    destination = dest;

    cout << "\n================================================================================" << endl;
    cout << "Navigation Started" << endl;
    cout << "================================================================================" << endl;
    cout << "Robot ID  : " << robotID << endl;
    cout << "Order ID  : " << orderID << endl;
    cout << "Item At   : " << itemZone << " - " << itemAisle << " - " << itemShelf << endl;
    cout << "Deliver To: " << destination << endl;
    cout << "--------------------------------------------------------------------------------" << endl;

    // Push each route step onto the stack
    cout << "\nForward Path - Recording Steps:" << endl;
    cout << "--------------------------------------------------------------------------------" << endl;
 
    for (int i = 0; i < routeSize; i++) {
        route[i].stepNumber = i + 1;  // assign step numbers
        if (pathStack.push(route[i])) {
            cout << "Step " << route[i].stepNumber
                 << " : " << route[i].zone;
            if (!route[i].aisle.empty()) {
                cout << " - " << route[i].aisle;
            }
            if (!route[i].shelf.empty()) {
                cout << " - " << route[i].shelf;
            }
            cout << endl;

            //Random obstacle simulation (20% each step)
            if (i < routeSize - 1 && rand() % 5 == 0) {
                cout << "\nObstacle Detected at Step " << route[i].stepNumber << "!" << endl;
                cout << "Path is blocked - initiating backtrack" << endl;
                handleObstacle();

                //Push current steps
                route[i].stepNumber = pathStack.getSize() + 1;
                pathStack.push(route[i]);
                cout << "Resuming path - Step " << route[i].stepNumber << " re-recorded." << endl;
            }
        }
    }

    pathStarted = true;
    itemReached = true;
 
    cout << "--------------------------------------------------------------------------------" << endl;
    cout << "Robot arrived at: " << itemZone  << " - " << itemAisle << " - " << itemShelf << endl;
    cout << "Item picked up successfully!" << endl;
    cout << "================================================================================" << endl;
}

// ================================================================
//  NAVIGATION SYSTEM — recordStep()
//  Records one manual movement step onto the stack.
//  Used for standalone testing when Task 5 is not yet integrated.
//
//  Time Complexity  : O(1)
//  Space Complexity : O(1)
// ================================================================
bool NavigationSystem::recordStep(string zone,
                                  string aisle,
                                  string shelf) {
    if (pathStack.isFull()) {
        cout << "[Navigation] ERROR: Path is full. Cannot record more steps.\n";
        return false;
    }
 
    stepMovement step;
    step.stepNumber = pathStack.getSize() + 1;
    step.zone       = zone;
    step.aisle      = aisle;
    step.shelf      = shelf;
 
    pathStack.push(step);
 
    cout << "Step "  << step.stepNumber
         << " recorded : " << zone;
    if (!aisle.empty()) cout << " - " << aisle;
    if (!shelf.empty()) cout << " - " << shelf;
    cout << "\n";

    // ---- Random Obstacle Simulation — 20% chance ----
    if (rand() % 5 == 0) {
        cout << "\nObstacle detected at Step "
            << step.stepNumber << "!" << endl;
        cout << "  Path is blocked - initiating backtrack" << endl;
        handleObstacle();
    }

    pathStarted = true;
    return true;
}

