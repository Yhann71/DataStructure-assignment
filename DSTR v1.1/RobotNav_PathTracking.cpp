//Pops all steps from stack and reverses each steps.
//Start from last step to first step
void NavigationSystem::returnPath() {
    if (pathStack.isEmpty()) {
        cout << "No path to reverse. Robot is already at start." << endl;
        return;
    }

    cout << "\n================================================================================" << endl;
    cout << "Return Path - Retracing Path in Reverse" << endl;
    cout << "--------------------------------------------------------------------------------" << endl;
    cout << "Robot : " << robotID << endl;
    cout << "Order : " << orderID << endl;
    cout << "Return: " << destination << endl;
    cout << "--------------------------------------------------------------------------------" << endl;

    cout << left
        << setw(8) << "Step"
        << setw(16) << "Zone"
        << setw(16) << "Aisle"
        << setw(16) << "Shelf" << endl;
    cout << "--------------------------------------------------------------------------------" << endl;

    int returnStep = 1;
    while (!pathStack.isEmpty()) {
        stepMovement step = pathStack.pop();

        cout << left
            << setw(8)  << returnStep
            << setw(16) << step.zone
            << setw(16) << (step.aisle.empty() ? "-" : step.aisle)
            << setw(16) << (step.shelf.empty() ? "-" : step.shelf) << endl;
        returnStep++;
    }
 
    cout << "--------------------------------------------------------------------------------" << endl;
    cout << robotID << " has returned to starting position." << endl;
    cout << "Order " << orderID << " completed successfully!" << endl;
    cout << "================================================================================" << endl;

    // Reset after successful return
    pathStarted = false;
    itemReached    = false;
}

//To handle if obstacle is in path
//Pop last step that led to obstacle
//Ask and push the new direction then continue path
bool NavigationSystem::handleObstacle() {
    if (pathStack.isEmpty()) {
        cout << "Navigation cannot backtrack as no steps recorded." << endl;
        return false;
    }

    // Step 1 — Pop last step (backtrack)
    stepMovement last = pathStack.pop();
 
    cout << "\n================================================================================" << endl;
    cout << "Obstacle has been detected! - Backtracking" << endl;
    cout << "--------------------------------------------------------------------------------" << endl;
    cout << "Obstacle at Step " << last.stepNumber << " : " << last.zone;
    if (!last.aisle.empty()) {
        cout << " - " << last.aisle;
    }
    if (!last.shelf.empty()) {
        cout << " - " << last.shelf;
    }
    cout << endl;

    // Step 2 — Show current position after backtrack
    cout << "\nRobot backtracked to" << endl;
    if (!pathStack.isEmpty()) {
        stepMovement current = pathStack.peek();
        cout << "  " << current.zone;
        if (!current.aisle.empty()) {
            cout << " - " << current.aisle;
        } 
        if (!current.shelf.empty()) {
            cout << " - " << current.shelf;
        }
    } else {
        cout << "Starting position (no previous steps)";
    }
    cout << endl;
    

    // Step 3 — Ask for new direction
    cout << "\nEnter new direction to avoid obstacle: " << endl;
    string newZone, newAisle, newShelf;
    cout << "Zone  : "; getline(cin, newZone);
    cout << "Aisle : "; getline(cin, newAisle);
    cout << "Shelf : "; getline(cin, newShelf);

    // Step 4 — Push new step onto stack
    stepMovement newStep;
    newStep.stepNumber = pathStack.getSize() + 1;
    newStep.zone       = newZone;
    newStep.aisle      = newAisle;
    newStep.shelf      = newShelf;
 
    pathStack.push(newStep);

    // Step 5 — Confirm new direction recorded
    cout << "\nNew Step " << newStep.stepNumber << " recorded : " << newZone;
    if (!newAisle.empty()) {
        cout << " - " << newAisle;
    }
    if (!newShelf.empty()) {
        cout << " - " << newShelf;
    }
    cout << "\nContinuing path from new location." << endl;
    cout << "Remaining steps in path: " << pathStack.getSize() << endl;
    cout << "--------------------------------------------------------------------------------" << endl;
 
    return true;
}

//Clears all state and empties the stack for the next task
void NavigationSystem::resetNavigation() {
    // Empty the stack by creating a fresh one
    pathStack = stack();
    robotID = "";
    orderID = "";
    destination = "";
    itemZone = "";
    itemAisle = "";
    itemShelf = "";
    pathStarted = false;
    itemReached = false;
 
    cout << "Navigation System reset successful. Ready for next task." << endl;
}

